# create  list using [] brackets
my_list_1 = ["S","T","Fish",9,10]
# print the list
print(my_list_1)
# change the item at index number 2 'Fish' to 'Chips'
my_list_1[2] = "Chips"
# print the list with new item at index 2
print(my_list_1)

# lists can contain duplicates
my_list_3 = ["cat", "dog", "horse", "pig", "dog"]
print(my_list_3)
