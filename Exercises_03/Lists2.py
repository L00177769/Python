# create the first list using [] brackets
my_list_1 = [1,2,3,4,"A"]
# create the second list
my_list_2 = ["S","T","Fish",9,10]
# list1 and list2 combined using concatenation (+ sign) to make a new list
concatenated_list = my_list_1 + my_list_2
# print the new combined list
print(concatenated_list)
# show data type of list
print(type(concatenated_list))

# list containing different types
my_list_3 = ["abc", 94, True, 66, "female"]
print(my_list_3)
print(type(my_list_3))