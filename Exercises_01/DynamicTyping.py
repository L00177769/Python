# create variable with integer value
dog = 2 
# print variable value 
print(dog)
# print(type) command to print variable value type
print(type(dog)) 
# change variable value to a string
dog = "mutt" 
# print variable value
print(dog)
# print(type) command to print variable value type
print(type(dog))
print()

# functions to change number to string
a = str(45)
print(a)
print(type(a))

# function to change int to float
a = float(45)
print(a)
print(type(a))

# function to change float to integer
a = int (45.6)
print(a)
print(type(a))

