# print title
print("Test Arithmetic Operators")
# create variable with integer values
a = 3 
b = 2
# prints varible name and value
print("a = ",a) 
print("b = ",b)
# print variables and perform calculations using  Arithmetic Operators **, *, /, %, //, +, -
print("a ** b =", a**b, "(Exponentiation)") # Exponentiation (**)
print("a * b =", a*b, "(Multiplication)") # Multiplication (*) 
print("a / b =", a/b, "(Division)") # Division (/)
print("a % b =", a%b, "(Modulus)") # Modulus (%)
print("a // b =", a//b, "(Floor Division)") # Floor Division (//)
print("a + b =", a+b, "(Addition)") # Addition (+)
print("a - b =", a-b, "(Subtraction)") # Subtraction (-)
print()
# use brackets () to change default order or precendence
print("Test Precedence Example") 
